    private void addListnerToJTable() {
        jTable1.addMouseListener(new MouseAdapter() {
            public void mousePressed(MouseEvent mouseEvent) {
                JTable table = (JTable) mouseEvent.getSource();
                Point point = mouseEvent.getPoint();
                int row = table.rowAtPoint(point);
                if (mouseEvent.getClickCount() == 2 && table.getSelectedRow() != -1) {
                    String itemName = jTable1.getValueAt(row, 0).toString();
                    String quantity = jTable1.getValueAt(row, 1).toString();
                    String unit_rate = jTable1.getValueAt(row, 2).toString();

                    txt_itemname.setText(itemName);
                    txt_quantity.setText(quantity);
                    txt_unitrate.setText(unit_rate);
                    //btn_add.setEnabled(true);
                    //btn_add.setText("Click here to Update Item");
                    //updateFlat=true;
                    deletSelectedRow();
                }
            }
        });
    }

