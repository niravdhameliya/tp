    public boolean isValidNumber(int key) {
        boolean flag = false;
        if ((key > 47 && key < 58) || (key > 95 && key < 106)) {
            flag = true;
        } else if (key == 8 || key == 46) {
            flag = true;
        }
        return flag;
    }

    private ArrayList<QuatationItems> getJTableData() {
        System.out.println("getJTableData Called");
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        ArrayList<QuatationItems> items = new ArrayList();
        if (model != null) {
            for (int count = 0; count < model.getRowCount(); count++) {
                //numdata.add();
                String item_name = model.getValueAt(count, 0).toString();
                String quantity = model.getValueAt(count, 1).toString();
                String unit_rate = model.getValueAt(count, 2).toString();
                QuatationItems quatationItem = new QuatationItems(item_name, quantity, unit_rate);
                items.add(quatationItem);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Please add items first");
        }
        System.out.println(items);
        return items;
    }
