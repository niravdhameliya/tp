    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int rowSelected = jTable1.getSelectedRow();
        if (rowSelected > -1) {
            DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel(); 
            model1.removeRow(rowSelected);
            supplyOrderItemsList.remove(rowSelected);
            calcGrandTotal();
        } else {
            JOptionPane.showMessageDialog(this, "Select a record to delete");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        workOrderNo = txtWONo.getText();
        particulars = txtParticulars.getText();
        quantity = txtQuantity.getText();
        basic_rate = txtBasic.getText();
        total_rate = txtTotal.getText();
        model = (DefaultTableModel) jTable1.getModel();
        Vector row = new Vector();
        row.add(workOrderNo);
        row.add(particulars);
        row.add(quantity + " " + comboOptions.getSelectedItem());
        row.add(basic_rate);
        row.add(total_rate);

        model.addRow(row);

        SupplyOrderItems soi = new SupplyOrderItems(workOrderNo, particulars, quantity + " " + comboOptions.getSelectedItem(), basic_rate, total_rate);
        supplyOrderItemsList.add(soi);

        calcGrandTotal();
    }//GEN-LAST:event_jButton1ActionPerformed

