    public void calcGrandTotal() {
        double amnt = calcAmount();
        amnt = amnt - Double.parseDouble(txtDiscountAmount.getText().trim());
        double other = Double.parseDouble(txtOtherCharges.getText().trim());
        amnt = amnt + other;
        txtSubTotal.setText(amnt + "");
        double cgst = Double.parseDouble(txtCgst.getText().toString());
        double sgst = Double.parseDouble(txtSgst.getText().toString());
        double igst = Double.parseDouble(txtIgst.getText().toString());

        double cgstval = ((amnt) / 100) * cgst;
        double sgstval = ((amnt) / 100) * sgst;
        double igstval = ((amnt) / 100) * igst;
        lblCgst.setText(cgstval + "");
        lblSgst.setText(sgstval + "");
        lblIgst.setText(igstval + "");

        amnt = amnt + cgstval + sgstval + igstval;
        txtGrandTotal.setText(amnt + "");

        long i = Math.round(amnt);
        txtGrandTotal.setText(i + "");
        String s = NumberToWordsConverter.convert((int) i);
        lblWords.setText("In words: " + s + " only");
    }

    public double calcAmount() {
        double fAmount = 0;
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        for (int count = 0; count < model.getRowCount(); count++) {
            double amnt = Double.parseDouble(model.getValueAt(count, 4).toString());
            fAmount = fAmount + amnt;
        }
        return fAmount;
    }

