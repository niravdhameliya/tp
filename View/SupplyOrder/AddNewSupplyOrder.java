    public AddNewSupplyOrder(String id) {
        initComponents();
        jTable1.setFont(new Font("Serif", Font.PLAIN, 20));
        Customer currentCustomer = UtilVars.getCustomer();

        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        Date date = new Date();
        String currDate = formatter.format(date);

        txtSODate.setText(currDate);
        txtRefDate.setText(currDate);

        txtSONo.setText(id);
        txtPAN.setText(currentCustomer.getPanNo());
        txtGSTIN.setText(currentCustomer.getGstin());
        txtContact.setText(currentCustomer.getContact());
        String cgst = currentCustomer.getCgst();
        String sgst = currentCustomer.getSgst();
        String igst = currentCustomer.getIgst();
        if (Integer.parseInt(cgst) > 0) {
            txtCgst.setText(cgst);
        } else {
            txtCgst.setText("0");
            txtCgst.setEnabled(false);
        }
        if (Integer.parseInt(sgst) > 0) {
            txtSgst.setText(sgst);
        } else {
            txtSgst.setText("0");
            txtSgst.setEnabled(false);
        }
        if (Integer.parseInt(igst) > 0) {
            txtIgst.setText(igst);
        } else {
            txtIgst.setText("0");
            txtIgst.setEnabled(false);
        }

        supplyOrderItemsList = new ArrayList<>();
        txtSupplierDetails.setText(UtilVars.getCustomer().getName() + "\n" + UtilVars.getCustomer().getAddress());
    }

