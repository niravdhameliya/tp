    private void btn_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addActionPerformed
        nomenclature = txtNomenclature.getText();
        quantity = txtQuantity.getText();
        remarks = txtRemarks.getText();

        model = (DefaultTableModel) jTable1.getModel();
        Vector row = new Vector();
        row.add(nomenclature);
        row.add(quantity + " " + comboOptions.getSelectedItem());
        row.add(remarks);
        model.addRow(row);

        TechnicalBidItems tbi = new TechnicalBidItems(nomenclature, quantity + " " + comboOptions.getSelectedItem(), remarks);
        technicalBidItemsList.add(tbi);
    }//GEN-LAST:event_btn_addActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int rowSelected = jTable1.getSelectedRow();
        if (rowSelected > -1) {
            DefaultTableModel model1 = (DefaultTableModel) jTable1.getModel();
            technicalBidItemsList.remove(rowSelected);
            model1.removeRow(rowSelected);
        } else {
            JOptionPane.showMessageDialog(this, "Select a record to delete");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

