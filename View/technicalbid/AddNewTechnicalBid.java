    private void loadTechnicalBidToTable() {
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        while (dm.getRowCount() > 0) {
            dm.removeRow(0);
        }
        ArrayList<TechnicalBidItems> data1 = UtilVars.getTechnicalBid().getTechnicalBidItems();
        System.out.println("row:" + data1.size());
        for (int i = 0; i < data1.size(); i++) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Vector row = new Vector();
            row.add(data1.get(i).getNomenclature());
            row.add(data1.get(i).getQuantity());
            row.add(data1.get(i).getRemarks());
            model.addRow(row);
        }
    }

